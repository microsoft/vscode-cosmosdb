/**
 * vite-plugin-react-refresh-preamble
 *
 * Dev-only. Injects the React Refresh runtime preamble required by
 * `@vitejs/plugin-react` so HMR works inside the VS Code webview.
 *
 * Why a plugin and not an inline <script> in BaseTab.ts
 * -----------------------------------------------------
 * The webpack build had `@pmmmwh/react-refresh-webpack-plugin` quietly emit
 * the runtime into the dev bundle. With Vite, plugin-react expects HTML
 * authors to call its `transformIndexHtml` hook to inject the preamble — but
 * our HTML is generated server-side in `BaseTab.ts`, not through Vite, so
 * that path is unavailable. We previously inlined the preamble code directly
 * into the HTML template, which mixed dev-only Vite plumbing into extension-
 * host code that should know nothing about the bundler. This plugin moves
 * that concern back where it belongs: the bundler config.
 *
 * How it works (mirrors vite-plugin-monaco-workers)
 * -------------------------------------------------
 *   1. Exposes a virtual module `virtual:react-refresh-preamble` whose body
 *      is the standard plugin-react preamble (installs the refresh hooks on
 *      `window` before any React module runs).
 *   2. Injects `import 'virtual:react-refresh-preamble';` as the FIRST
 *      statement of the webview entry (`src/webviews/index.tsx`). ESM hoists
 *      imports in source order, so the preamble executes before React.
 *   3. `apply: 'serve'` — the plugin is inactive in production builds, so the
 *      production bundle contains no preamble code at all.
 *
 * The preamble imports `/@react-refresh`, which Vite serves from the dev
 * server. Since `views.js` is itself loaded from the dev server in dev mode,
 * the relative URL resolves correctly. The webview's dev CSP already allows
 * the dev server origin under `script-src` / `default-src`.
 */

const VIRTUAL_ID = 'virtual:react-refresh-preamble';
const RESOLVED_VIRTUAL_ID = '\0' + VIRTUAL_ID;

/** Standard preamble from @vitejs/plugin-react — installs Refresh hooks on `window`. */
const PREAMBLE_CODE = `
// virtual:react-refresh-preamble — generated by vite-plugin-react-refresh-preamble
import RefreshRuntime from "/@react-refresh";
RefreshRuntime.injectIntoGlobalHook(window);
window.$RefreshReg$ = () => {};
window.$RefreshSig$ = () => (type) => type;
window.__vite_plugin_react_preamble_installed__ = true;
`;

/**
 * @returns {import('vite').Plugin}
 */
export function reactRefreshPreamble() {
    /** Normalised absolute path of the webview entry. */
    let entryId = '';

    return {
        name: 'react-refresh-preamble',
        // Dev-only: no preamble in production bundles.
        apply: 'serve',
        enforce: 'pre',

        configResolved(config) {
            entryId = (config.root + '/src/webviews/index.tsx').replace(/\\/g, '/');
        },

        // ── Virtual module: resolves `virtual:react-refresh-preamble` ───────────
        resolveId(id) {
            if (id === VIRTUAL_ID) return RESOLVED_VIRTUAL_ID;
            return null;
        },

        load(id) {
            if (id !== RESOLVED_VIRTUAL_ID) return null;
            return PREAMBLE_CODE;
        },

        // ── Inject `import 'virtual:react-refresh-preamble';` at the top of the entry ──
        //
        // Source-order import hoisting guarantees the preamble runs before any
        // React module in the graph.
        transform(code, id) {
            const normId = id.replace(/\\/g, '/').split('?')[0];
            if (normId !== entryId) return null;
            if (code.includes(VIRTUAL_ID)) return null; // idempotent guard
            return {
                code: `import ${JSON.stringify(VIRTUAL_ID)};\n${code}`,
                map: null,
            };
        },
    };
}
