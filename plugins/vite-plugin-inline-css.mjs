/**
 * Inlines every bundled `.css` asset into the entry chunk and removes the
 * separate `.css` files from the output. At runtime, the entry script creates
 * a single `<style>` tag and appends it to `document.head` before any UI
 * renders.
 *
 * Why?  The VS Code webview HTML is generated by the extension (BaseTab.ts)
 * and there is no clean way to add extra `<link rel="stylesheet">` tags.
 * Mirroring what webpack's `style-loader` does — "CSS travels through JS" —
 * keeps the webview boot path down to a single `<script type="module">`.
 *
 * Scope:
 *   - Production only (`apply: 'build'`). In dev Vite already serves CSS via
 *     HMR-managed `<style>` tags.
 *   - Single entry chunk (we have exactly one: `views.js`).
 *   - All CSS sources concatenated into one `<style>` element.
 *
 * Why not `vite-plugin-css-injected-by-js`?
 *   That upstream package supports multi-entry queues, dynamic removal,
 *   custom selectors, dev HMR, etc. — none of which we need. This local
 *   plugin is ~30 lines of code and trivially debuggable.
 *
 * @returns {import('vite').Plugin}
 */
export function inlineCss() {
    return {
        name: 'inline-css',
        apply: 'build',
        // `enforce: 'post'` lets us run AFTER all CSS transforms — including
        // PostCSS, SCSS preprocessing, and Rolldown's own asset emission —
        // so we can see the final emitted CSS contents.
        enforce: 'post',

        generateBundle(_opts, bundle) {
            // 1) Collect every emitted .css asset in deterministic order.
            const cssEntries = Object.entries(bundle).filter(
                ([name, item]) => item.type === 'asset' && name.endsWith('.css'),
            );
            if (cssEntries.length === 0) return;

            const css = cssEntries
                .map(([, item]) => (typeof item.source === 'string' ? item.source : item.source.toString('utf8')))
                .join('\n');

            // 2) Remove the .css assets — no separate stylesheets in dist.
            for (const [name] of cssEntries) {
                delete bundle[name];
            }

            // 3) Locate the entry chunk and prepend a tiny injector. The
            //    injector creates a single <style> element on first execution.
            //    `JSON.stringify` gives a safely-escaped JS string literal for
            //    any CSS content (handles `</style>`, backticks, unicode, etc.).
            const entry = Object.values(bundle).find((item) => item.type === 'chunk' && item.isEntry);
            if (!entry || entry.type !== 'chunk') {
                this.warn('[inline-css] no entry chunk found — CSS not injected');
                return;
            }

            const injector =
                'try{if(typeof document!=="undefined"){' +
                'var __css_el=document.createElement("style");' +
                `__css_el.textContent=${JSON.stringify(css)};` +
                'document.head.appendChild(__css_el);' +
                '}}catch(e){console.warn("[inline-css] injector failed",e);}\n';

            entry.code = injector + entry.code;
        },
    };
}
